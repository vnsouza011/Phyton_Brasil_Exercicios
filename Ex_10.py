# 10 - Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus Farenheit.

print("Conversor de temperatura Celsius para Farenhenit")

graus_celsius = float(input("Digite a temperatura Celsius: "))

graus_farenheint = (graus_celsius * 1.8) + 32

print("a conversão de: ", graus_celsius,"ºC  para ", graus_farenheint,"ºF")