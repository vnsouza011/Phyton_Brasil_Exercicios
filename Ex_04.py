# 04 - Faça um Programa que peça as 4 notas bimestrais e mostre a média.

bim01 = float(input("Digite a nota do primeiro bimestre: "))

bim02 = float(input("Digite a nota do segundo bimestre: "))

bim03 = float(input("Digite a nota do terceiro bimestre: "))

bim04 = float(input("Digite a nota do quarto bimestre: "))

media = (bim01 + bim02 + bim03 + bim04)/4

print("Sua média anual é :", media)